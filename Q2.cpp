#include "Question.h"
#include <iostream>
#include <string>
#include <fstream>

using namespace std;

int main()
{
	int p1 = 0, p2 = 0, i;

	Question Q[10];
	Question :: rquestions(Q);
	cout << "-----------------------------------------------------------------\n";
	cout << "                     Questions For Player 1\n";
	cout << "-----------------------------------------------------------------\n";
	for (i = 0; i < 5; i++) {
		Q->DisplayQuestion(Q, i, p1);
	}
	cout << "-----------------------------------------------------------------\n";
	cout << "Score for Player 1: " << p1 << endl;
	cout << "-----------------------------------------------------------------\n\n";

	cout << "-----------------------------------------------------------------\n";
	cout << "                     Questions For Player 2\n";
	cout << "-----------------------------------------------------------------\n";
	for (; i < 10; i++) {
		Q->DisplayQuestion(Q, i, p2);
	}
	cout << "-----------------------------------------------------------------\n";
	cout << "Score for Player 2: " << p2 << endl;
	cout << "-----------------------------------------------------------------\n\n";

	if (p1 > p2) {
		cout << "-----------------------------------------------------------------\n";
		cout << "Player 1 has won by " << p1 - p2 <<" points!" << endl;
		cout << "-----------------------------------------------------------------\n";
	}
	else if (p2 > p1) {
		cout << "-----------------------------------------------------------------\n";
		cout << "Player 2 has won by " << p2 - p1 << " points!" << endl;
		cout << "-----------------------------------------------------------------\n";
	}
	else {
		cout << "-----------------------------------------------------------------\n";
		cout << "It is a tie between both the players!" << endl;
		cout << "-----------------------------------------------------------------\n";
	}
	return 0;
}



